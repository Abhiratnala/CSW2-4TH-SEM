package com.iter.ratnalaabhignya.universityclass;

public class student {
	String name;
	int roll;
	public student(String n, int r) {
		name=n;
		roll=r;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getRoll() {
		return roll;
	}

	public void setRoll(int roll) {
		this.roll = roll;
	}


}
